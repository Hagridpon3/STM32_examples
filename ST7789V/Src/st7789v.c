{
  uint8_t i;
  uint16_t index;
  uint8_t ypos;
  uint8_t xpos;
  uint16_t logopointer;
  uint8_t logobyte;

  // logo width 29 bytes
  // logo heigth 157 bytes
  logopointer = 0;
  for (ypos = 0; ypos < 157; ypos++)
  {
    st7789v_set_cursor(4, 159+ypos);

    send_intruction(RAMWR);
    CS_low();
    RS_data(); // RS = H

    for (xpos = 0; xpos < 29; xpos++)
    {
      logobyte = logo_map[logopointer++];

      for (i = 0; i < 8; i++)
      {// pro kazdy bit zleva doprava
        if (logobyte & (0x80 >> i))
        {
          // bit je jednicka
        	SPI2_Write((color>>8) & 0xFF);
        	SPI2_Write(color & 0xFF);
        }
        else
        {
          // bit je nula
        	SPI2_Write((BACKGROUND>>8) & 0xFF);
        	SPI2_Write(BACKGROUND & 0xFF);
        }
      }
    }
    CS_high();
  }
}



