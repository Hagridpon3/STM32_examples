void print_char(uint8_t character, uint8_t xpos, uint16_t ypos, uint16_t color, uint8_t inv)
{
  uint8_t line;
  uint16_t input;
  uint8_t bitpos;
  uint8_t xposlimit;

  for (line = 0; line < CHARHEIGHT; line++)
  {

    st7789v_set_cursor(xpos,ypos+line);        // novy mikroradek pro vykresleni znaku
    xposlimit = xpos;

    input = ASCII12x12_Table[CHARHEIGHT*(character-' ')+line];
    send_intruction(RAMWR);           // zapis pixelu
    CS_low();
    RS_data(); // RS = H

    for (bitpos = 0; bitpos < CHARWIDTH; bitpos++)	// bylo 8 misto CHARWIDTH
    {// pro kazdy bit zleva doprava

      if (xposlimit++ < DISPXSIZE)
      {
        if (input & (0x8000 >> bitpos))
        {
          // bit je jednicka
          if (inv == NOINV)
          {
            // primy rezim
        	  SPI2_Write((color>>8) & 0xFF);
        	  SPI2_Write(color & 0xFF);
          }
          else
          {
            // inverzni rezim
        	  SPI2_Write((BACKGROUND>>8) & 0xFF);
        	  SPI2_Write(BACKGROUND & 0xFF);
          }
        }
        else
        {
          // bit je nula
        	SPI2_Write((BACKGROUND>>8) & 0xFF);
        	SPI2_Write(BACKGROUND & 0xFF);
        }
      }
    }
    CS_high();
  }
}

void text_print(uint8_t x, uint16_t y, uint8_t *text, uint16_t color, uint8_t mode, uint16_t maxwidth)
{
  uint8_t i, length;

  // pro celou delku retezce nebo maximalni sirku okna vypis text po znacich
  if (CHARWIDTH*strlen((const char*)text) > maxwidth)
  {
	  length = maxwidth / CHARWIDTH;
  }
  else
  {
	  length = strlen((const char*)text);
  }
  for (i = 0; i < length; i++)
  {
    if (x+i*CHARWIDTH < DISPXSIZE)
    {
      print_char(text[i], x+i*CHARWIDTH, y, color, mode);
    }
  }
}

