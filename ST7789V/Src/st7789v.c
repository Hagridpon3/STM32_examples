void st7789v_window_clear(uint16_t startx, uint16_t endx, uint16_t starty, uint16_t endy, uint16_t color)
{
	uint16_t x,y;

	for (y = starty; y < endy; y++)
	{
		st7789v_set_cursor(startx, y);
		send_intruction(RAMWR);           // zapis pixelu
		CS_low();
		RS_data(); // RS = H
		for (x = startx; x < endx; x++)
		{
			SPI2_Write((color>>8) & 0xFF);
			SPI2_Write(color & 0xFF);
		}
		CS_high();
	}
}

void st7789v_set_cursor(uint8_t xpos, uint16_t ypos)
{
  send_intruction(CASET);           // nastaveni adresy sloupce
  send_data(0x00);                  // CASET byte 0
  send_data(xpos);                  // CASET byte 1
  send_data(0x00);                  // CASET byte 2
  send_data(0xEF);                  // CASET byte 3

  send_intruction(RASET);           // nastaveni adresy radku
  send_data((ypos >> 8) & 0xFF);    // RASET byte 0
  send_data(ypos & 0xFF);           // RASET byte 1
  send_data(0x01);                  // RASET byte 2
  send_data(0x3F);                  // RASET byte 3
}

